package com.sea.weather.date.nmc.model;

public class HjForecastTimeVO {
	
	// 流速
	private String speed;
	// 方向
	private String direction;
	// 浪高
	private String waveheight;
	// 涌高
	private String surgeheight;
	// 海温
	private String sst;
	
	public String getSpeed() {
		return speed;
	}
	public void setSpeed(String speed) {
		this.speed = speed;
	}
	public String getDirection() {
		return direction;
	}
	public void setDirection(String direction) {
		this.direction = direction;
	}
	public String getWaveheight() {
		return waveheight;
	}
	public void setWaveheight(String waveheight) {
		this.waveheight = waveheight;
	}
	public String getSurgeheight() {
		return surgeheight;
	}
	public void setSurgeheight(String surgeheight) {
		this.surgeheight = surgeheight;
	}
	public String getSst() {
		return sst;
	}
	public void setSst(String sst) {
		this.sst = sst;
	}
	
	
}
